# LESSON 8: IF statements


day_of_week = input("What day is it?")                    # Uses semi-colon

if day_of_week == "Monday":
    print("\nHave a great start to your week!")
elif day_of_week == "Tuesday":
    print("\nHave a great Tuesday start to your week!")
else:
    print("\nNot Monday or Tuesday but it'll still be a great day!")


